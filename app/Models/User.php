use Illuminate\Database\Eloquent\Factories\HasFactory;
use Illuminate\Foundation\Auth\User as Authenticatable;
use Illuminate\Notifications\Notifiable;

class User extends Authenticatable
{
    /** @use HasFactory<\Database\Factories\UserFactory> */
    use HasFactory, Notifiable;

    /**
     * The attributes that are mass assignable.
     *
     * @var list<string>
     */
    protected $fillable = [
        'name',
        'email',
        'password',
        'role'
    ];

    public function products() { return $this->hasMany(Product::class); }
    public function submittedValidationRequests() { return $this->hasMany(ValidationRequest::class, 'submitted_by'); }
    public function assignedValidationRequests() { return $this->hasMany(ValidationRequest::class, 'assigned_admin'); }

    public function isAdmin(): bool { return $this->role === 'Admin'; }
    public function isUMKM(): bool { return $this->role === 'UMKM'; }

